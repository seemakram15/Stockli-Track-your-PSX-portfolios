/** Returns unique AMC names in alphabetical order. */
export function getAmcList(): string[] {
  return [...new Set(PAKISTAN_FUNDS.map((f) => f.amc))].sort();
}

/** Returns funds for a given AMC. */
export function getFundsByAmc(amc: string): PakistanFund[] {
  return PAKISTAN_FUNDS.filter((f) => f.amc === amc);
}

/** Looks up the AMC for a given fund name. */
export function getAmcForFund(fundName: string): string {
  return PAKISTAN_FUNDS.find((f) => f.name === fundName)?.amc ?? "";
}
